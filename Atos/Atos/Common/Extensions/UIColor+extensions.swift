import UIKit

extension UIColor {
    internal enum Atos {
        internal static let backButton = UIColor(named: "backButton")
        internal static let background = UIColor(named: "background")
        internal static let black = UIColor(named: "black")
        internal static let white = UIColor(named: "white")
    }
}
